package primitives;

public class CharPractice {

    public static void main(String[] args) {


        char letter = 'T'; // values of the char data type has to be in '' (single quotation mark)

        //char letter2 = "A"; it will not compile

        System.out.println(letter); // T --> ASCII table dec value for letter T is 84

        char letter3 = '9';
        System.out.println(letter3); // 9 ---> ASCII table dec value for 9 is 57

        char symbol = '*';
        System.out.println(symbol); // *

        char space  = ' ';
        System.out.println(space);
        System.out.println(symbol); //

        int x =4;
        System.out.println(x); // 4

        //===============
        int something = 'A';  // A is 65 in ASCII Table
        System.out.println(something); // 65
        char something2 = 99;   // c
        System.out.println(something2) ; // c

        char random = 500;
        System.out.println(random);//

    }
}
